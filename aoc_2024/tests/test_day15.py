def test_move():
    grid, directions = parse_input(SMALL_EXAMPLE)
    assert find_robot(grid) == Point(2, 2)

    expected_positions = [
        Point(2, 2),
        Point(2, 1),
        Point(2, 1),
        Point(3, 1),
        Point(4, 1),
        Point(4, 1),
        Point(4, 2),
    ]
    for expected, direction in zip(expected_positions, directions):
        assert move(grid, direction) == expected


